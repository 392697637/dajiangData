-- ==============================================
-- 函数名：gis_generate_3d_grid
-- 功能描述：生成三维网格节点表。根据传入的GeoJSON面范围、高程范围和分辨率，
--          自动计算经纬度边界并生成均匀分布的3D网格点，每个点包含空间坐标（经纬度+高度）及其索引。
-- 参数说明：
--   p_project_id           : 项目ID（必传）。表名变为 gis_grid_nodes_<project_id>，实现项目级数据隔离。
--   p_geojson              : GeoJSON面（Polygon），函数自动解析并计算最小/最大经纬度范围。
--   p_min_alt, p_max_alt   : 高程范围（米），例如 50, 280
--   p_resolution           : 网格分辨率（米），表示相邻网格点之间的水平/垂直间距
-- 返回值：标准TABLE结构
--   code        integer     返回码：200成功，400参数错误，500执行异常
--   table_name  text        生成的网格表名
--   msg         text        返回信息/错误提示
--   count       bigint      生成网格点总数量
-- ==============================================
SELECT * FROM gis_generate_3d_grid(
    '2c95908e958f3b75019593551f520126',
    '{"type":"Polygon","coordinates":[[[112.70,34.20],[114.20,34.20],[114.20,35.00],[112.70,35.00],[112.70,34.20]]]}',
    50,300,100
);

 
-- ============================================================
-- 函数名称： gis_electric_fence_project
-- 函数功能： 动态创建项目专属电子围栏表，并自动导入相交的禁飞区、试飞区数据
-- 参数说明：
--   p_project_id    text        输入参数：项目唯一ID（用于生成表名）
--   p_geom_json     text        输入参数：项目范围的GeoJSON多边形字符串
-- 返回值： 标准TABLE结构
--   code        integer     状态码：200=执行成功 400=参数错误/无数据 500=执行异常
--   msg         varchar     状态描述信息
--   tablename   varchar     生成的项目电子围栏表名
--   count       bigint      导入围栏数据总条数
-- ============================================================
SELECT* FROM gis_electric_fence_project(
    '2c95908e958f3b75019593551f520126', --  输入参数：项目唯一ID（用于生成表名）
    '{"type":"Polygon","coordinates":[[[113.0,34.5],[114.0,34.5],[114.0,35.0],[113.0,35.0],[113.0,34.5]]]}'  -- 输入参数：项目范围的GeoJSON多边形字符串
);

-- ==============================================
-- 函数名：gis_mark_electric_fence
-- 功能描述：根据电子围栏表 bo_electric_fence 中的数据，更新三维网格表中每个点的 zone_type。
--          根据围栏的 fence_type 字段进行映射：
--            '1' → '禁飞区'
--            '2' → '管控区'
--            '3' → '适飞区'
--          当一个网格点同时落在多个围栏内时，按照优先级选取：禁飞区 > 管控区 > 适飞区。
-- 参数：p_project_id - 项目ID（可选，空字符串或NULL表示操作默认表 gis_grid_nodes）
-- 返回值：标准TABLE结构
--   code        integer     返回码：200成功，400参数错误，500执行异常
--   table_name  text        操作的网格表名
--   msg         text        执行结果描述
--   count       bigint      更新的记录行数
-- 优化点：
--   - 使用临时表存储受影响的网格ID及其应设置的最高优先级区域类型。
--   - 利用 ST_Intersects 空间连接和 DISTINCT ON + 排序实现每个网格仅选优先级最高的区域。
--   - 批量更新仅修改 zone_type 实际发生变化的行，减少写IO。
--   - 通过 project_id 过滤围栏数据，支持多项目隔离。
-- 注意事项：
--   - 调用前需确保三维网格表已通过 gis_generate_3d_grid 生成。
--   - 围栏数据必须包含有效的 geometry 和 height（限制高度），且 fence_type 在 ('1','2','3') 范围内。
-- ==============================================
 SELECT * FROM gis_mark_electric_fence('2c95908e958f3b75019593551f520126');
 
 
-- ========================================================================================校验电子围栏==========================================================================================
-- =============================================
-- 函数名称： gis_check_electric_fence
-- 函数功能： 电子围栏空间冲突校验（禁飞区/管控区/试飞区互斥规则校验）
-- 参数说明：
--   p_project_id    varchar     项目ID（可选），用于区分项目专属围栏表
--   p_fence_type    text        围栏类型：1=禁飞区，2=管控区，3=试飞区
--   p_lng_lat_alt   text        坐标GeoJSON字符串，支持Feature或直接Geometry
-- 返回值： 标准TABLE结构
--   code               integer     返回码：200成功，400参数错误，500空间冲突
--   table_name         text        冲突对应的表名
--   orig_fence_type    text        传入的原始围栏类型
--   conflict_fence_type text       冲突的围栏类型(数字)
--   msg                text        详细提示信息（区分相交/包含 + 中文名称）
--   new_geom           text        标准化后的新围栏几何JSON
--   conflict_geom      text        冲突围栏的几何JSON
-- 调用说明：
--   1. 第一个参数：project_id，用于校验项目专属围栏表 gis_electric_fence_{project_id} 及业务表 bo_electric_fence
--   2. 第二个参数：fenceType，1=禁飞区，2=管控区，3=试飞区
--   3. 第三个参数：lngLatAlt，支持GeoJSON Feature，也支持直接传Polygon/MultiPolygon等Geometry字符串
-- =============================================
-- 示例1：新增试飞区(3)，传Feature格式的项目范围。
SELECT * FROM gis_check_electric_fence(
  '2c95908e958f3b75019593551f520126',
  '3',
  '{"type":"Feature","geometry":{"type":"Polygon","coordinates":[[[113.289609,34.951427,0],[113.290607,34.615358,0],[113.979944,34.596458,0],[114.013926,34.930172,0]]]},"properties":{}}'
);
